"use client"

import { SharedChatLayout } from "@/components/shared-chat-layout"
import { TrendingUp, Target, BarChart3, Users, Lightbulb, DollarSign } from "lucide-react"

const suggestedTopics = [
  { icon: DollarSign, text: "売上を伸ばしたい" },
  { icon: Target, text: "経営戦略を見直したい" },
  { icon: TrendingUp, text: "利益率を改善したい" },
  { icon: Users, text: "組織づくりについて相談" },
  { icon: BarChart3, text: "市場分析をしてほしい" },
  { icon: Lightbulb, text: "新規事業を検討している" },
]

const STORAGE_KEY = "symphony-management-consultant-chat-history"

const INITIAL_MESSAGE = `はじめまして。経営コンサルタントの田中と申します。

中古車販売業界で15年以上、経営戦略の策定から実行支援まで幅広くサポートしてまいりました。

御社の経営課題について、一緒に考えていければと思います。まずは現状をお聞かせください。

- 今、一番の経営課題は何でしょうか？
- 売上・利益の推移はいかがですか？
- 組織面で気になることはありますか？

どんな些細なことでも構いません。お気軽にご相談ください。`

const generateManagementConsultantResponse = (question: string, messageCount: number): string => {
  const q = question.toLowerCase()

  if (messageCount <= 4) {
    if (q.includes("売上") || q.includes("売れない") || q.includes("伸びない") || q.includes("伸ばし")) {
      return `売上についてのご相談ですね。ありがとうございます。

まず、売上を構成する要素を整理しましょう。

**売上 = 来店数 x 成約率 x 平均単価**

この3つのうち、どこにボトルネックがあるかを把握することが最初のステップです。

具体的にお聞きしたいのですが：
1. **月間の来店数**（問い合わせ含む）はどのくらいですか？
2. **成約率**（来店から購入に至る割合）は把握されていますか？
3. **平均販売単価**はいくらくらいでしょうか？

これらの数字がわかると、どこを改善すれば最もインパクトが大きいか、具体的にご提案できます。もし正確な数字がわからなくても、だいたいの感覚値で構いません。`
    }

    if (q.includes("在庫") || q.includes("回転") || q.includes("売れ残")) {
      return `在庫回転率の改善は、キャッシュフローに直結する重要なテーマですね。

在庫管理の基本原則を共有させてください。

**【在庫管理の3つのKPI】**
1. **在庫回転率**：年間売上原価 / 平均在庫金額
2. **平均在庫日数**：365日 / 在庫回転率
3. **適正在庫数**：月間販売台数 x 2.0〜2.5

業界の健全な目安として、**在庫日数45日以内**を目指すのが良いとされています。

現在の御社の状況として：
- 在庫台数は何台くらいですか？
- 月間の販売台数はどのくらいですか？
- 仕入れから販売までの平均日数は把握されていますか？

データを基に、適正在庫の水準と、回転率を上げるための具体策をご提案します。`
    }

    if (q.includes("利益") || q.includes("粗利") || q.includes("コスト") || q.includes("経費")) {
      return `利益改善のご相談ですね。非常に重要なテーマです。

利益を改善するアプローチは大きく3つあります：

**1. 粗利率の向上**
- 仕入れ価格の最適化（仕入チャネルの見直し）
- 付帯商品の提案強化（コーティング、保証、保険等）
- 適正な価格設定（相場分析に基づく値付け）

**2. 販管費の最適化**
- 広告宣伝費のROI分析
- 人件費の適正配分
- 固定費の見直し

**3. 販売台数の増加**
- 既存顧客からの紹介促進
- Web集客の強化
- 成約率の改善

まずは現在の **粗利率** と **営業利益率** を教えていただけますか？業界標準と比較して、改善の優先順位をご提案します。`
    }

    if (q.includes("組織") || q.includes("人材") || q.includes("採用") || q.includes("社員") || q.includes("スタッフ")) {
      return `組織づくりに関するご相談ですね。

中古車販売業界では、人材の質が業績に直結します。

**組織課題を整理するフレームワーク：**

1. **採用**：どんな人材を、どのチャネルで採用していますか？
2. **育成**：入社後の教育体制はありますか？
3. **定着**：離職率はどのくらいですか？
4. **評価**：報酬・評価制度は整備されていますか？

よくある課題として：
- 「良い人が来ない」→ 採用ブランディングの問題
- 「すぐ辞めてしまう」→ オンボーディングや職場環境の問題
- 「成長しない」→ 育成の仕組みの問題

御社の組織について、一番気になっている点はどこでしょうか？具体的な状況をお聞かせいただければ、優先的に取り組むべきポイントをご提案します。`
    }

    return `ご相談ありがとうございます。

興味深いテーマですね。もう少し詳しくお聞かせいただけますか？

具体的には：
- **現状**：今どのような状況でしょうか？
- **課題**：一番困っていることは何ですか？
- **目標**：どのような状態を目指したいですか？

これらを整理することで、より具体的なアドバイスが可能になります。遠慮なくお話しください。`
  }

  // 中盤以降
  if (q.includes("戦略") || q.includes("計画") || q.includes("ビジョン")) {
    return `経営戦略の策定は、まさにコンサルタントの本領発揮の領域です。

**戦略策定の5ステップ：**

**Step 1: 現状分析（SWOT分析）**
- 強み：御社ならではの競争優位は？
- 弱み：改善すべきポイントは？
- 機会：市場で活かせるチャンスは？
- 脅威：注意すべきリスクは？

**Step 2: ビジョン設定**
- 3年後にどんな会社にしたいか？

**Step 3: 重点テーマの決定**
- 最大3つに絞る

**Step 4: アクションプランの策定**
- 誰が、いつまでに、何をするか

**Step 5: KPIの設定とモニタリング**
- 進捗を測る指標を決める

まず Step 1 から一緒に取り組みましょう。御社の強みは何だとお考えですか？`
  }

  if (q.includes("集客") || q.includes("広告") || q.includes("マーケティング") || q.includes("sns") || q.includes("web")) {
    return `集客・マーケティングのご相談ですね。

現代の中古車販売において、**オンライン集客**は必須です。

**集客チャネルの優先順位（推奨）：**

1. **Googleビジネスプロフィール**（最優先）
   - 無料で始められ、効果が高い
   - クチコミ対策が重要

2. **自社ポータルサイト（カーセンサー、グーネット等）**
   - 掲載内容の質を上げる
   - 写真枚数、コメントの充実

3. **SNS（Instagram、TikTok）**
   - 車両紹介だけでなく、スタッフの人柄が伝わる投稿
   - 定期的な更新が鍵

4. **自社サイト / SEO**
   - 中長期的な資産になる

現在、どのチャネルに力を入れていらっしゃいますか？月間の広告予算はどのくらいでしょうか？`
  }

  if (q.includes("差別化") || q.includes("競合") || q.includes("強み")) {
    return `競合との差別化は、持続的な成長のために不可欠ですね。

**差別化の3つの軸：**

1. **商品力での差別化**
   - 特定車種・ジャンルへの特化（SUV専門、輸入車専門等）
   - 仕入れルートの独自性
   - 品質基準の明確化

2. **サービス力での差別化**
   - 購入後のアフターサポート
   - 保証制度の充実
   - 納車体験の演出

3. **ブランド力での差別化**
   - 地域での認知度・信頼度
   - クチコミ・紹介の仕組み
   - オーナーや社員の魅力発信

「何でも売れます」は、実は「何も強みがない」と同義です。

御社が最も自信を持って提供できる価値は何でしょうか？それを軸に差別化戦略を練りましょう。`
  }

  if (q.includes("ありがとう") || q.includes("助かり") || q.includes("参考")) {
    return `こちらこそ、ご相談いただきありがとうございます。

お話を伺っていて、御社には十分なポテンシャルがあると感じています。

**次のステップとして提案させてください：**

1. 今日お話しした内容を**アクションリスト**に落とし込む
2. 優先順位の高いものから**1つずつ**着手する
3. **2週間後**に進捗を振り返る

経営改善は一朝一夕にはいきませんが、正しい方向に一歩ずつ進めば、必ず結果は出ます。

また何かあればいつでもご相談ください。一緒に御社の成長を実現していきましょう。`
  }

  return `なるほど、よく理解できました。

このテーマについて、いくつかの観点から考えてみましょう。

**まず確認したいこと：**
- この課題は、いつ頃から感じていらっしゃいますか？
- これまでに何か対策は取られましたか？
- 理想的な状態はどのようなイメージでしょうか？

経営課題は複合的に絡み合っていることが多いです。表面的な対処ではなく、根本原因を見つけることが重要です。

もう少し詳しくお聞かせいただければ、より具体的なご提案ができます。`
}

